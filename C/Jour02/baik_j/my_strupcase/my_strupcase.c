char	*my_strupcase(char str[])
{
  int	i;

  for (i = 0; str[i] != '\0'; i = i + 1)
    {
      if (str[i] >= 'a' && str[i] <= 'z')
	{
	  str[i] -= 'a' - 'A';
	}
    }
  return (str);
}
